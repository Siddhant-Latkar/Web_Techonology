// ==============================
// COMPARISON OPERATORS
// ==============================

console.log(10 == "10");  
// true because only value is checked

console.log(10 === "10"); 
// false because data type is also checked

console.log(20 != 10);    
// true because values are different

console.log(20 > 10);     
// true because 20 is greater

console.log(5 < 10);      
// true because 5 is smaller

console.log(10 >= 10);    
// true because equal allowed

console.log(5 <= 2);      
// false because 5 is greater than 2
